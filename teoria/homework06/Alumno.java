package teoria.homework06;

public class Alumno {
  private String name;
  private int age;
  private double average;

  public void setName(String str) {
    name = str;
  }
  public String getName() {
    return name;
  }
  public void setAge(int n) {
    age = n;
  }
  public int getAge() {
    return age;
  }
  public void setAverage(double n) {
    average = n;
  }
  public double getAverage() {
    return average;
  }
}