package teoria.homework17;

public class Color {
  private int green;
  private int blue;
  private int red;

  public Color(int g, int b, int r) {
    green = g;
    blue = b;
    red = r;
  }

  public int getGreen() {
    return green;
  }
  public int getRed() {
    return red;
  }
  public int getBlue() {
    return blue;
  }
}
